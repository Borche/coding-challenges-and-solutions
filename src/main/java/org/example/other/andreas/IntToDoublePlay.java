package org.example.other.andreas;

public class IntToDoublePlay {

  public static void main(String[] args) {
    System.out.println(3 / 2);
    System.out.println(3.0 / 2);
    System.out.println(3 / 2.0);
    System.out.println(3.0 / 2.0);

    int a1 = 3 / 2;
    double a2 = 3.0 / 2;
    double a3 = 3 / 2.0;
    double a4 = 3.0 / 2.0;
  }
}
